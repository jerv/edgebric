#!/usr/bin/env node
/**
 * Bundle the API server + core + types into a single JS file for the desktop app.
 *
 * Native modules (better-sqlite3, sharp, etc.) are left external —
 * they're included via electron-builder's extraResources + a pruned node_modules.
 *
 * Usage: node scripts/bundle-server.mjs
 * Output: packages/desktop/resources/server/server.js
 */
import { build } from "esbuild";
import fs from "fs";
import path from "path";
import { execSync } from "child_process";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

// Modules that can't be bundled:
// - Native addons (better-sqlite3, sharp, sqlite-vec)
// - Pino ecosystem (uses worker threads + dynamic requires internally)
const nativeExternals = [
  "better-sqlite3",
  "sharp",
  "sqlite-vec",
  "pino",
  "pino-http",
  "pino-pretty",
  "thread-stream",
];

// Node built-ins are automatically external in node platform

await build({
  entryPoints: [path.join(root, "packages/api/src/server.ts")],
  bundle: true,
  platform: "node",
  target: "node20",
  format: "esm",
  outfile: path.join(root, "packages/desktop/resources/server/server.js"),
  external: nativeExternals,
  // Resolve workspace packages
  alias: {
    "@edgebric/types": path.join(root, "shared/types/src/index.ts"),
    "@edgebric/core": path.join(root, "packages/core/src/index.ts"),
    "@edgebric/core/ingestion": path.join(root, "packages/core/src/ingestion/index.ts"),
    "@edgebric/core/rag": path.join(root, "packages/core/src/rag/index.ts"),
  },
  // Ignore .node native addon imports — they're resolved at runtime
  loader: { ".node": "empty" },
  sourcemap: false,
  minify: false, // Keep readable for debugging
  banner: {
    js: [
      '// Bundled Edgebric API server — generated by scripts/bundle-server.mjs',
      'import { createRequire } from "module";',
      'const require = createRequire(import.meta.url);',
    ].join("\n"),
  },
});

console.log("✓ Server bundled to packages/desktop/resources/server/server.js");

// Copy the web frontend build into the expected location relative to the bundle.
// server.ts resolves webDistDir as ../../web/dist relative to its own directory,
// so from resources/server/ that's resources/web/dist/
const webSrc = path.join(root, "packages/web/dist");
const webDest = path.join(root, "packages/desktop/resources/web/dist");

if (fs.existsSync(webSrc)) {
  fs.rmSync(webDest, { recursive: true, force: true });
  fs.mkdirSync(path.dirname(webDest), { recursive: true });
  execSync(`cp -R "${webSrc}" "${webDest}"`);
  console.log("✓ Web frontend copied to packages/desktop/resources/web/dist/");
} else {
  console.warn("⚠ Web build not found at packages/web/dist/ — run pnpm build first");
}
